package com.sj.timetable;

public class ClassInfo {
    private float sid;
    private String sname;
    private String teacher;
    
    public float getSessionid() {
        return sid;
    }
    public void setSessionid(float sid) {
        this.sid= sid;
    }
    public String getSubjName() {
        return sname;
    }
    public void setSubjName(String sname) {
        this.sname = sname;
    }
    public String getTeacher() {
        return teacher;
    }
    public void setTeacher(String tname) {
        this.teacher = tname;
    }
   
}
